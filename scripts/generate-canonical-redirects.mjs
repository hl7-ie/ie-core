import fs from 'node:fs';
import path from 'node:path';

const siteRoot = path.resolve(process.argv[2] ?? 'site');
const repoName =
  process.env.GITHUB_REPOSITORY?.split('/').pop() ||
  path.basename(process.cwd());

const deployments = [
  { artifactDir: siteRoot, fallbackPage: 'index.html' },
  { artifactDir: path.join(siteRoot, 'R5'), fallbackPage: path.join('R5', 'index.html') }
].filter(({ artifactDir }) => fs.existsSync(artifactDir));

const createdRedirects = new Set();
const redirectMarker = '<!-- generated by generate-canonical-redirects.mjs -->';
// The marker is emitted on line 2 of the redirect template, so the first 3 lines
// are sufficient to verify that an existing index.html was generated by this script.
const redirectHeaderLines = 3;

function readResource(filePath) {
  try {
    return JSON.parse(fs.readFileSync(filePath, 'utf8'));
  } catch {
    return null;
  }
}

function toSiteRelative(filePath) {
  return path.relative(siteRoot, filePath);
}

function toPosix(filePath) {
  return filePath.split(path.sep).join('/');
}

// GitHub Pages project sites are served from /<repo>/, but the published artifact
// already represents that root, so canonical URL paths must drop the repository prefix.
function stripRepositoryPrefix(relativeUrlPath) {
  const trimmedPath = relativeUrlPath.replace(/^\/+/, '');
  if (trimmedPath === repoName) {
    return '';
  }
  if (trimmedPath.startsWith(`${repoName}/`)) {
    return trimmedPath.slice(repoName.length + 1);
  }
  return trimmedPath;
}

function escapeHtml(value) {
  return value
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#39;');
}

function createRedirect(redirectDir, targetPath, canonicalUrl) {
  const redirectFile = path.join(redirectDir, 'index.html');
  const relativeTarget = toPosix(path.relative(redirectDir, targetPath)) || 'index.html';
  const safeRelativeTarget = escapeHtml(relativeTarget);
  const safeCanonicalUrl = escapeHtml(canonicalUrl);

  if (fs.existsSync(redirectFile)) {
    const existingHeader = fs
      .readFileSync(redirectFile, 'utf8')
      .split('\n')
      .slice(0, redirectHeaderLines)
      .join('\n');
    if (!existingHeader.includes(redirectMarker)) {
      throw new Error(`Refusing to overwrite existing non-generated file: ${redirectFile}`);
    }
  }

  fs.mkdirSync(redirectDir, { recursive: true });
  fs.writeFileSync(
    redirectFile,
    `<!DOCTYPE html>
${redirectMarker}
<html lang="en">
<head>
  <meta charset="utf-8">
  <title>Redirecting...</title>
  <meta http-equiv="refresh" content="0; url=${safeRelativeTarget}">
  <link rel="canonical" href="${safeCanonicalUrl}">
</head>
<body>
  <p>Redirecting to <a href="${safeRelativeTarget}">${safeRelativeTarget}</a>.</p>
</body>
</html>
`
  );

  createdRedirects.add(toSiteRelative(redirectFile));
}

function resolveTarget(resource, artifactDir, fallbackPage) {
  if (resource.resourceType === 'ImplementationGuide') {
    const fallbackPath = path.join(siteRoot, fallbackPage);
    return fs.existsSync(fallbackPath) ? fallbackPath : null;
  }

  const stem = `${resource.resourceType}-${resource.id}`;
  const candidates = [
    `${stem}.html`,
    `${stem}.json.html`,
    `${stem}.json`
  ];

  for (const candidate of candidates) {
    const candidatePath = path.join(artifactDir, candidate);
    if (fs.existsSync(candidatePath)) {
      return candidatePath;
    }
  }

  const fallbackPath = path.join(siteRoot, fallbackPage);
  return fs.existsSync(fallbackPath) ? fallbackPath : null;
}

for (const { artifactDir, fallbackPage } of deployments) {
  const jsonFiles = fs
    .readdirSync(artifactDir)
    .filter((fileName) => fileName.endsWith('.json'))
    .sort();

  for (const jsonFile of jsonFiles) {
    const resource = readResource(path.join(artifactDir, jsonFile));
    if (!resource?.resourceType || !resource?.id || !resource?.url) {
      continue;
    }

    const targetPath = resolveTarget(resource, artifactDir, fallbackPage);
    if (!targetPath) {
      continue;
    }

    let canonicalUrl;
    try {
      canonicalUrl = new URL(resource.url);
    } catch {
      console.warn(`Skipping invalid canonical URL for ${jsonFile}: ${resource.url}`);
      continue;
    }

    const redirectDir = path.join(siteRoot, stripRepositoryPrefix(canonicalUrl.pathname));
    createRedirect(redirectDir, targetPath, canonicalUrl.href);

    if (resource.resourceType === 'ImplementationGuide') {
      const canonicalParentPath = path.posix.dirname(canonicalUrl.pathname);
      const canonicalGrandparentPath = path.posix.dirname(canonicalParentPath);
      const baseDir = path.join(siteRoot, stripRepositoryPrefix(canonicalGrandparentPath));
      createRedirect(
        baseDir,
        path.join(siteRoot, fallbackPage),
        new URL(canonicalGrandparentPath, canonicalUrl.origin).href
      );
    }
  }
}

console.log(`Created ${createdRedirects.size} canonical redirect pages under ${siteRoot}`);
